#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	*s1;

	s1 = (char *)str;
	i = 0;
	while (*s1 != c)
	{
		if (*s1 == '\0')
			return (NULL);
		s1++;
	}
	return (s1);
}
